#include<stdio.h>
int main()
{
    printf("This is space |' '|\n");
    printf("This is horizontal tab |'\t'|\n");
    printf("This is newline |'\n'|\n");
    printf("This is vertical tab |'\v'|\n");
    printf("This is feed |'\f'|\n");
    printf("This is carriage return |'\r'|\n");
    return 0;
}
